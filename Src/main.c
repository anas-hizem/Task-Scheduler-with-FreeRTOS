/* Read data from DHT11 sensor */
void ReadDHT11(void)
{
  int result ;//= DHT11_Read(&temperature, &humidity);
  if(result == DHT11_OK)
  {
    // Send data over UART for debugging
    char buffer[50];
    snprintf(buffer, sizeof(buffer), "Temp: %.2f C, Humidity: %.2f%%\r\n", temperature, humidity);
    HAL_UART_Transmit(&huart2, (uint8_t*)buffer, strlen(buffer), HAL_MAX_DELAY);
  }
  else
  {
    // Handle sensor read error
    HAL_UART_Transmit(&huart2, (uint8_t*)"Error reading DHT11\r\n", 21, HAL_MAX_DELAY);
  }
}

/* Measure distance using HC-SR04 */
void ReadHC_SR04(void)
{
  HC_SR04_GetDistance();
  // Send distance data over UART for debugging
  char buffer[50];
  snprintf(buffer, sizeof(buffer), "Distance: %.2f cm\r\n", distance);
  HAL_UART_Transmit(&huart2, (uint8_t*)buffer, strlen(buffer), HAL_MAX_DELAY);
}

/* LED alert logic based on sensor data */
void LED_Alert(void)
{
  if (temperature > alertThresholdTemp || distance < alertThresholdDist)
  {
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);  // Turn ON LED
  }
  else
  {
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);  // Turn OFF LED
  }
}
void HC_SR04_GetDistance(void)
{
    // Send a 10us HIGH pulse on Trigger pin
    HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_SET);
    delay_us(10);
    HAL_GPIO_WritePin(TRIG_PORT, TRIG_PIN, GPIO_PIN_RESET);

    // Wait for Echo to go HIGH (start of pulse)
    while (HAL_GPIO_ReadPin(ECHO_PORT, ECHO_PIN) == GPIO_PIN_RESET);

    start_time = __HAL_TIM_GET_COUNTER(&htim2);  // Capture start time

    // Wait for Echo to go LOW (end of pulse)
    while (HAL_GPIO_ReadPin(ECHO_PORT, ECHO_PIN) == GPIO_PIN_SET);

    end_time = __HAL_TIM_GET_COUNTER(&htim2);  // Capture end time

    // Calculate the distance (speed of sound = 340 m/s or 0.034 cm/us)
    distanceObject = (end_time - start_time) * 0.034 / 2.0;
    // Préparer et envoyer les données de distance via UART
    char msg[50];
    snprintf(msg, sizeof(msg), "Distance: %.2f cm\n", distanceObject);
    HAL_UART_Transmit(&huart2, (uint8_t*)msg, strlen(msg), HAL_MAX_DELAY);
}

void delay_us(uint16_t us)
{
    __HAL_TIM_SET_COUNTER(&htim2, 0);  // Reset the timer counter
    while (__HAL_TIM_GET_COUNTER(&htim2) < us);  // Wait for the counter to reach 'us'
}

